package practice7;

class StudentA
{
	private String name;
	private int sno;
	private String dept;
	private int grade;
	private double credit;
	public StudentA(String name, int sno, String dept, int grade, double credit) {
		super();
		this.name = name;
		this.sno = sno;
		this.dept = dept;
		this.grade = grade;
		this.credit = credit;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public double getCredit() {
		return credit;
	}
	public void setCredit(double credit) {
		this.credit = credit;
	}
	
}

